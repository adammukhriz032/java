package adam_mukhiz;

class switch2 {
	public static void main (String args[]) {
		int umur;
		umur = 3;
		switch(umur){
		case 0:
			//case 0 maksud 1 tahun
			System.out.println("adik saya boleh merangkak");
			break;
		case 1:
			//case 1 maksud 2 tahun
			System.out.println("adik saya tak boleh merangkak");
			break;
		case 2:
			System.out.println("adik saya masih kecil");
			break;
		case 3:
			System.out.println("saya tak kisah");
		
		
		
		
		
		
		
		}
		
	}
}
